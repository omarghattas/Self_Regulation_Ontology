c_re = re.compile('(%s)' % '|'.join(cList.keys()))

def expandContractions(text, c_re=c_re):
    def replace(match):
        return cList[match.group(0)]
    return c_re.sub(replace, text)

def loadGloveModel(gloveFile):
    print("Loading Glove Model")
    f = open(gloveFile,'r')
    model = {}
    for line in f:
        splitLine = line.split()
        word = splitLine[0]
        embedding = np.array([float(val) for val in splitLine[1:]])
        model[word] = embedding
    print("Done.",len(model)," words loaded!")
    return model

def remove_punctuation(text):
    translator = str.maketrans('', '', string.punctuation)
    return text.translate(translator)

def tf_idf(docs, tokenize):
    tfidf = TfidfVectorizer(tokenizer=tokenize, min_df=3,
                        max_df=0.90, max_features=30000,
                        use_idf=True, sublinear_tf=True,
                        norm='l2');
    tfidf.fit(docs);
    return tfidf;

# strips text down
cachedStopWords = nltk.corpus.stopwords.words("english")
lemma = nltk.wordnet.WordNetLemmatizer()
def tokenize(text, contractions=False, lemmatize=False, vocab=None):
    min_length = 3
    text = remove_punctuation(text)
    if not contractions:
        text = expandContractions(text)
    words = map(lambda word: word.lower(), nltk.word_tokenize(text));
    words = [word for word in words if word not in cachedStopWords]
    if lemmatize:
        words = (list(map(lambda token: lemma.lemmatize(token), words)))
    if vocab:
        words = [word for word in words if vocab.get(word) is not None]
    p = re.compile('[a-zA-Z]+');
    filtered_tokens = list(filter(lambda token:
                          p.match(token) and len(token)>=min_length, words));
    return filtered_tokens